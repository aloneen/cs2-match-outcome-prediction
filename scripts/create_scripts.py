from docx import Document
from docx.shared import Pt, RGBColor, Cm
from docx.enum.text import WD_ALIGN_PARAGRAPH
from docx.oxml.ns import qn
from docx.oxml import OxmlElement


def set_margins(doc):
    for section in doc.sections:
        section.top_margin = Cm(2)
        section.bottom_margin = Cm(2)
        section.left_margin = Cm(2.5)
        section.right_margin = Cm(2.5)


def add_header(doc, name, slides_range):
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    r = p.add_run("SCRIPT  //  CS2 MATCH OUTCOME PREDICTION  //  NARXOZ 2026")
    r.font.size = Pt(9)
    r.font.color.rgb = RGBColor(0x99, 0x99, 0x99)
    r.bold = True

    p2 = doc.add_paragraph()
    p2.alignment = WD_ALIGN_PARAGRAPH.CENTER
    p2.paragraph_format.space_before = Pt(6)
    r2 = p2.add_run(name)
    r2.font.size = Pt(26)
    r2.bold = True

    p3 = doc.add_paragraph()
    p3.alignment = WD_ALIGN_PARAGRAPH.CENTER
    r3 = p3.add_run(slides_range)
    r3.font.size = Pt(11)
    r3.font.color.rgb = RGBColor(0x66, 0x66, 0x66)

    doc.add_paragraph()
    hr = doc.add_paragraph("=" * 68)
    hr.paragraph_format.space_after = Pt(12)
    hr.runs[0].font.size = Pt(8)
    hr.runs[0].font.color.rgb = RGBColor(0xBB, 0xBB, 0xBB)


def slide_block(doc, num, title):
    p = doc.add_paragraph()
    p.paragraph_format.space_before = Pt(22)
    p.paragraph_format.space_after = Pt(10)
    pPr = p._p.get_or_add_pPr()
    shd = OxmlElement("w:shd")
    shd.set(qn("w:val"), "clear")
    shd.set(qn("w:color"), "auto")
    shd.set(qn("w:fill"), "1A1A1A")
    pPr.append(shd)
    r = p.add_run(f"   SLIDE {num}  —  {title}   ")
    r.bold = True
    r.font.size = Pt(11)
    r.font.color.rgb = RGBColor(0xFF, 0xFF, 0xFF)


def line(doc, text, bold=False):
    p = doc.add_paragraph()
    p.paragraph_format.space_before = Pt(1)
    p.paragraph_format.space_after = Pt(3)
    p.paragraph_format.left_indent = Cm(0.7)
    r = p.add_run(text)
    r.font.size = Pt(12)
    r.bold = bold
    return p


def gap(doc):
    p = doc.add_paragraph()
    p.paragraph_format.space_before = Pt(0)
    p.paragraph_format.space_after = Pt(4)


